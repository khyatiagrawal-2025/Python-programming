#Single matrix
import numpy
a=numpy.array([[1,2,3],[4,5,6]])
print("Sum =",numpy.sum(a))
print("Product =",numpy.prod(a))
print("Max =",numpy.max(a))
print("Min =",numpy.min(a))
print("variance =",numpy.var(a))
print("Mean =",numpy.mean(a))
print("Mean(standard deviation) =",numpy.std(a))
print("Median =",numpy.median(a))